# Dada la tupla `numeros = (1, 2, 2, 3, 4, 2, 5)`, cuenta cuántas veces aparece el número 2.

numeros = (1, 2, 2, 3, 4, 2, 5)
conteo = numeros.count(2)
print(f'El número 2 aparece {conteo} veces')

# Explicación: El método count() devuelve el número de ocurrencias de un valor.
