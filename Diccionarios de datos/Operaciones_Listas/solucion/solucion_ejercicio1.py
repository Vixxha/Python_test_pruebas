# Crea una lista con los números del 1 al 5. Agrega el número 6 al final y elimina el número 3. Imprime la lista resultante.

mi_lista = [1, 2, 3, 4, 5]
mi_lista.append(6)
mi_lista.remove(3)
print('Resultado:', mi_lista)

# Explicación: Se utiliza el método append() para agregar al final y remove() para eliminar el primer valor coincidente.
