# Invierte la lista `palabra = ['P', 'y', 't', 'h', 'o', 'n']` utilizando rebanadas (slicing) e imprime el resultado.

palabra = ['P', 'y', 't', 'h', 'o', 'n']
invertida = palabra[::-1]
print('Invertida:', invertida)

# Explicación: El paso -1 [::-1] recorre la lista en orden inverso.
